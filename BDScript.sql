	idComidaPersonal INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idProducto INT NOT NULL,
	KilosXMes FLOAT NOT NULL,
	CONSTRAINT FK_GastoComidaPersonal_Producto FOREIGN KEY (idProducto) REFERENCES Producto(id),
