CREATE TABLE FiestaChicas (
	idFiestaChicas INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idProducto INT NOT NULL,
	Cantidad FLOAT NOT NULL,
	CONSTRAINT FK_FiestaChicas_Producto FOREIGN KEY (idProducto) REFERENCES Producto(id),
