drop database SistemaInventarios;

CREATE DATABASE SistemaInventarios;

USE SistemaInventarios;

CREATE TABLE Producto (
	id INT IDENTITY(1,1) PRIMARY KEY,
	nombre TEXT NOT NULL,
	precio FLOAT NOT NULL,
	unidadMedida TEXT,
	tipo TEXT,
);

--Pagina mermas--
CREATE TABLE MermasMalEstado (
	idMerma INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idProducto	INT NOT NULL,
	cantidad FLOAT NOT NULL,
	CONSTRAINT FK_ProductosMalEstado_Producto FOREIGN KEY (idProducto) REFERENCES Producto(id),
); 

CREATE TABLE MermasCortes (
	idMerma INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idProducto	INT NOT NULL,
	cantidad FLOAT NOT NULL,
	CONSTRAINT FK_MermasCortes_Producto FOREIGN KEY (idProducto) REFERENCES Producto(id),
); 

CREATE TABLE Gasto (
	idGasto INT IDENTITY(1,1) PRIMARY KEY NOT NULL, 
	idProducto INT NOT NULL,
	Cantidad FLOAT NOT NULL,
	CONSTRAINT FK_Gasto_Producto FOREIGN KEY (idProducto) REFERENCES Producto(id),
);

