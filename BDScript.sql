--Nuevo cambio Mermas--

CREATE TABLE PlatosDevueltos (
	idPlatosDevueltos INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	NombreProducto INT NOT NULL,
	Cantidad FLOAT NOT NULL,
	Precio FLOAT NOT NULL,
);



--Pagina Verduras--

CREATE TABLE ReporteGastoVerdura (
	idReporteVerduras INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idProducto INT NOT NULL,
	CantidadGastoXDia FLOAT NOT NULL,
	dia INT NOT NULL,
	CONSTRAINT FK_ReporteGastoVerdura_Producto FOREIGN KEY (idProducto) REFERENCES Producto(id),
);

--Pagina Interno--

CREATE TABLE LicorConsumoInterno (
	idLicor INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	tipo FLOAT NOT NULL,
	nombre TEXT NOT NULL,
	precio FLOAT NOT NULL,
);

CREATE TABLE ConsumoInternoLicRest (
	idPlato INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idLicor INT NOT NULL,
	medida FLOAT NOT NULL,
	cantidad FLOAT NOT NULL,
	CONSTRAINT FK_ConsumoInternoLicRest_LicorConsumoInterno FOREIGN KEY (idLicor) REFERENCES LicorConsumoInterno(idLicor),
);

CREATE TABLE ConsumoInternoLicTiki (
	idPlato INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idLicor INT NOT NULL,
	medida FLOAT NOT NULL,
	cantidad FLOAT NOT NULL,
	CONSTRAINT FK_ConsumoInternoLicTiki_LicorConsumoInterno FOREIGN KEY (idLicor) REFERENCES LicorConsumoInterno(idLicor),
);

CREATE TABLE Plato (
	idPlato INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	nombre TEXT NOT NULL,
	cantidad FLOAT NOT NULL,
	gramaje FLOAT NOT NULL,
);

CREATE TABLE ConsumoInterno ( 
	idConsumoInterno INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	nombre TEXT NOT NULL,
	GRM FLOAT NOT NULL,
	costo FLOAT NOT NULL,
);



--Pagina Pedidos--

CREATE TABLE Pedido (
	idPedido INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	idProducto INT NOT NULL,
	dia TEXT NOT NULL, --(1-7, 8-14, 15-21, 22-28, 29-31)--
	CantidadPedido FLOAT NOT NULL,
	invInicial FLOAT NOT NULL,
	invFinal FLOAT NOT NULL,
	CONSTRAINT FK_Pedido_Producto FOREIGN KEY (idProducto) REFERENCES Producto(id),
); 

